-- Предположим, мы разрабатываем на Haskell интерфейс
-- системы перевода для естественных языков.
-- Он должен содержать функцию translate
-- с параметрами text, languageFrom и languageTo.
-- Расположите параметры в таком порядке,
-- чтобы было удобно реализовывать следующие функции:
-- translateFromSpanishToRussian,
-- translateFromEnglishToRussian
-- и translateToRussian

translate languageTo languageFrom text = "Translate text: " ++ text ++ " from " ++ languageFrom ++ " to " ++ languageTo
translateToRussian = translate "Russian"
translateFromSpanishToRussian = translate "Russian" "Spanish"
